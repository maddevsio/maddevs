<template>
  <div
    v-if="slice.items && slice.items.length"
    :class="slice.items[0].embed.type"
    class="embed"
  >
    <PrismicEmbed
      :field="this.embedFieldData"
      :class="`embed__${slice.items[0].embed.type}`"
      target="_blank"
    />
    <template v-if="slice.items[0].embed.type === 'link'">
      <div class="embed__image-wrapper">
        <div
          :style="{ backgroundImage: `url(${slice.items[0].embed.thumbnail_url})` }"
          class="embed__image"
        />
      </div>
    </template>
  </div>
</template>

<script>
export default {
  name: 'EmbedSlice',
  props: {
    slice: {
      type: Object,
      default: () => ({}),
    },
  },

  data() {
    return {
      embedFieldData: {},
    }
  },

  created() {
    const { items } = this.slice
    if (!items && !items.length) return

    const {
      embed: {
        url,
        title: rawTitle,
        html: rawHtml,
        type: embedType,
      },
      embed_title: embedTitle,
    } = items[0]

    if (rawHtml) {
      const matchDescription = rawHtml.match('<p>(.*?)</p>')
      const description = matchDescription ? matchDescription[1] || '' : ''

      const html = `
        <div data-type="website">
          <a href="${url}" target="_blank">
            <div class="embed__title">${embedTitle || rawTitle.split(' | ')[0]}</div>
            <p>${description}</p>
          </a>
        </div>
      `

      this.embedFieldData = {
        ...this.slice.embed,
        html,
      }
    }

    if (embedType === 'video') {
      const html = rawHtml
        .replace(/height="[0-9]*"/, 'height="500"')
        .replace(/width="[0-9]*"/, 'width="100%"')

      this.embedFieldData = {
        ...this.slice.embed,
        html,
      }
    }
  },
}
</script>

